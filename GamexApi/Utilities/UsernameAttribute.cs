        private static readonly Regex EmailOrUsernameRegex = CreateRegEx();

        public override bool IsValid(object value)
        {
            string strValue = value as string;
            if (!string.IsNullOrEmpty(strValue))
            {
                return EmailOrUsernameRegex.IsMatch(strValue);
            }
            return true;
        }

